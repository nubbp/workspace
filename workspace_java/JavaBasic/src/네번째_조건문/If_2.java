package 네번째_조건문;

public class If_2 {
    public static void main(String[] args) {
        System.out.println("프로그램 시작");

        int x = 7;

        // x의 값이 7이면 1을 출력하고, 그렇지 않으면 2를 출력하는 코드 작성
        if (x == 7) {
            System.out.println("1");
        } else {
            System.out.println("2");
        }


        System.out.println("프로그램 종료");
    }
}