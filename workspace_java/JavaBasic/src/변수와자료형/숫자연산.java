package 변수와자료형;

public class 숫자연산 {
    public static void main(String[] args) {
        System.out.println("10+10 = " + (10 + 10));
//        정수끼리의 연산 결과는 정수
        System.out.println("10/4 = " + 10 / 4);
//        실수가 포함되면 결과는 실수
        System.out.println("10/4.0 = " + 10 / 4.0);
        System.out.println("10/4*1.5 = " + 10 / 4 * 1.5);
        System.out.println("10.0/4*1.5 = " + 10.0 / 4 * 1.5);

    }
}
